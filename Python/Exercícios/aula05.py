def calcular_media():
 nota1 = float(input("Digite a primeira nota:"))
 nota2 = float (input("Digita a segunda nota:"))
 nota3 = float (input("Digita a terceira nota:"))
 nota4 = float (input("Digita a quarta nota:"))
 media = (nota1 + nota2 + nota3 + nota4 )/ 4 

 if media >= 7:
  print("Aprovado")

 else:
  print("Reprovado")
calcular_media()
calcular_media()